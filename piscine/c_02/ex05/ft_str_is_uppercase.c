int	ft_str_is_uppercase(char *str)
{
	int	p;

	p = 0;
	while (str[p] != '\0')
	{
		if (str[p] >= 'A' && str[p] <= 'Z')
			p++;
		else
			return (0);
	}
	return (1);
}
