int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	pos;

	pos = 0;
	while (s1[pos] != '\0' && s2[pos] != '\0' && s1[pos] == s2[pos] && n > 0)
	{
		pos++;
		n--;
	}
	if (n == 0)
		return (0);
	return (s1[pos] - s2[pos]);
}
